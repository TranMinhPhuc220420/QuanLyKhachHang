    private void updateTable() {
        this.model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Mã", "Họ Tên", "Địa Chỉ", "Số ĐT"});
        if (this.model.getRowCount() > 0) {
            this.model.getDataVector().removeAllElements();
        }
        for (KhachHang items : this.quanLyKhachHang.getListOfKhachHang()) {
            model.addRow(new String[]{items.getId(), items.getName(), items.getAddress(), items.getPhone()});
            this.jTable1.setModel(model);
        }
    }

