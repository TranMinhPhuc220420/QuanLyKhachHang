    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = this.jTable1.getSelectedRow();
        this.jTextField1.setText(this.jTable1.getValueAt(i, 0).toString());
        this.jTextField2.setText(this.jTable1.getValueAt(i, 1).toString());
        this.jTextField3.setText(this.jTable1.getValueAt(i, 2).toString());
        this.jTextField4.setText(this.jTable1.getValueAt(i, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            this.quanLyKhachHang.add(new KhachHang(
                    jTextField1.getText(),
                    jTextField2.getText(),
                    jTextField3.getText(),
                    jTextField4.getText()
            ));

            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thống báo", JOptionPane.INFORMATION_MESSAGE);

            clearTextInput();
            updateTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thống báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            if (this.quanLyKhachHang.find(jTextField1.getText()) != null && JOptionPane.showConfirmDialog(
                    null,
                    "Bạn có muốn xoá khách hàng có có Mã là :" + jTextField1.getText() + " không?",
                    "Thống báo",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                this.quanLyKhachHang.removeByID(jTextField1.getText());

                clearTextInput();
                updateTable();
                JOptionPane.showMessageDialog(null, "Xoá thành công", "Thống báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thống báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

